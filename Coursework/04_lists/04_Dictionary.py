
# Dictionaries in Python (key-value pairs)
# A dictionary stores data in pairs: key and value. Keys must be unique.

# Create a dictionary with some information
info = {
        "College": "NewLJ",
        "Branch": "CSE",
        "Code": 143
}

# Print the whole dictionary
print(info)

# Get the value for the key 'College'
print(info.get("College"))

# Get the value for the key 'Branch'
print(info.get("Branch"))

# Get the value for the key 'Code'
print(info.get("Code"))